// Grade calculator

Marks = 80;

if (Marks >= 90 && Marks <= 100 ){
    console.log("You Achieve Grade A")
}

else if (Marks >= 80 && Marks <=89 ){
    console.log("You Achieve Grade B")
}

else if (Marks >= 70 && Marks <=79 ){
    console.log("You Achieve Grade C")
}

else if (Marks >= 60 && Marks <=69 ){
    console.log("You Achieve Grade D")
}

else if (Marks >= 0 && Marks <=57 ){
    console.log("You Achieve Grade F")
}